package main

import (
	"bytes"
	"compress/zlib"
	"crypto/hmac"
	"crypto/sha256"
	"fmt"
	"hash"
	"log"
	"strings"
)

func main() {
	var (
		data       []byte
		message    []byte
		mac        hash.Hash
		hmacOctets []byte
		buf        *bytes.Buffer = new(bytes.Buffer)
		level                    = zlib.BestCompression
		zlibWriter *zlib.Writer
		n          int
		err        error
		zlibBytes  []byte
	)

	//message with timestamp and hmac zlib writer test program

	fmt.Printf("dataGPS: %#v\n", dataGPS)
	fmt.Println(strings.Repeat("-", 80))

	fmt.Printf("dataTimeStamp: %#v\n", dataTimeStamp)
	fmt.Println(strings.Repeat("-", 80))

	message = make([]byte, 0, 256)
	message = append(message, dataGPS...)
	message = append(message, dataTimeStamp...)

	fmt.Printf("message: %#v\n", message)
	fmt.Println(strings.Repeat("-", 80))

	data = make([]byte, 0, 256)
	data = append(data, message...)

	//HMAC ......
	mac = hmac.New(sha256.New224, exampleHmacSecret)
	mac.Write(exampleHmacSecret)
	mac.Write(message)
	mac.Write(exampleSaltSecret)
	hmacOctets = mac.Sum(nil)
	data = append(data, hmacOctets...)
	//end HMAC

	fmt.Printf("message+HMAC byte slice: %#v\n", data)
	fmt.Printf("# of bytes in the message+HMAC byte slice: %d\n", len(data))
	fmt.Println(strings.Repeat("-", 80))

	buf.Reset()
	//fmt.Printf("buf (before): %#v\n", buf)
	zlibWriter, err = zlib.NewWriterLevel(buf, level)
	if err != nil {
		log.Fatalln(err.Error())
	}
	if n, err = zlibWriter.Write(data); err != nil {
		log.Fatalln(err.Error())
	}
	zlibWriter.Flush()
	zlibWriter.Close()
	fmt.Printf("bytes written: %#v\n", n)
	//fmt.Printf("buf (after): %#v\n", buf)
	zlibBytes = buf.Bytes()
	fmt.Printf("The zlib compressed byte slice: %#v\n", zlibBytes)
	fmt.Printf("# of zlib bytes: %d\n", len(zlibBytes))
	fmt.Println(strings.Repeat("-", 80))
}

